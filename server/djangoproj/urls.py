from django.contrib import admin
from django.urls import path, include, re_path
from django.views.generic import TemplateView
from django.conf.urls.static import static
from django.conf import settings
from django.contrib.auth import views as auth_views
from djangoapp import views as app_views

urlpatterns = [
    path('admin/', admin.site.urls),
    path('djangoapp/', include('djangoapp.urls')),
    path('', TemplateView.as_view(template_name="Home.html")),
    path('about/', TemplateView.as_view(template_name="About.html")),
    path('contact/', TemplateView.as_view(template_name="Contact.html")),
    path('login/', auth_views.LoginView.as_view(template_name="login.html"), name='login'),
    path('api/login/', app_views.login_user, name='api_login'),
    path('api/register/', app_views.registration, name='api_register'),
    path('logout/', app_views.logout_request, name='logout'),
    path('register/', TemplateView.as_view(template_name="register.html"), name='register'),
]

# Add static and media file serving
urlpatterns += static(settings.STATIC_URL, document_root=settings.STATIC_ROOT)
urlpatterns += static(settings.MEDIA_URL, document_root=settings.MEDIA_ROOT)
